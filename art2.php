        <h1>The Role of Voluntary Non-Remunerated Blood Donation (VNRBD) in Healthcare</h1>

        <div class="image-container">
            <!-- Add the image you want to display here -->
            <img src="https://www.ifrc.org/sites/default/files/styles/article_press_release_featured_image/public/2021-07/p-UKR03-35_jpg%201280x500.jpg?itok=tR6A03VG" alt="Voluntary Non-Remunerated Blood Donation">
