        <h2>Introduction</h2>
        <p>
        A blood donation occurs when a person voluntarily has blood drawn and used for transfusions and/or made into biopharmaceutical medications by a process called fractionation (separation of whole blood components). Donation may be of whole blood, or of specific components directly (apheresis). Blood banks often participate in the collection process as well as the procedures that follow it.
        </p>

        <h2>Blood Donation</h2>
        <p>
        Charles Richard Drew (1904–1950) was an American surgeon and medical researcher. He researched in the field of blood transfusions, developing improved techniques for blood storage, and applied his expert knowledge to developing large-scale blood banks early in World War II. This allowed medics to save thousands of lives of the Allied forces. As the most prominent African American in the field, Drew protested against the practice of racial segregation in the donation of blood, as it lacked scientific foundation, and resigned his position with the American Red Cross, which maintained the policy until 1950
        </p>

        <h2>Obtaining the blood</h2>
        <p>
        There are two main methods of obtaining blood from a donor. The most frequent is to simply take the blood from a vein as whole blood. This blood is typically separated into parts, usually red blood cells and plasma, since most recipients need only a specific component for transfusions.
        </p>
      

        <h2>Recovery and time between donations</h2>
        <p>
        Donors are usually kept at the donation site for 10–15 minutes after donating since most adverse reactions take place during or immediately after the donation. Blood centers typically provide light refreshments, such as orange juice and cookies, or a lunch allowance to help the donor recover. The needle site is covered with a bandage and the donor is directed to keep the bandage on for several hours. In hot climates, donors are advised to avoid dehydration (strenuous exercise and games, alcohol) until a few hours after donation.
        </p>

        <h2>Donor helath benefits</h2>
        <p>
        In patients prone to iron overload, blood donation prevents the accumulation of toxic quantities.[119] Donating blood may reduce the risk of heart disease for men, but the link has not been firmly established and may be from selection bias because donors are screened for health problems.
        </p>

       
