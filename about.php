    <p id="para">A well organised Blood Transfusion Service (BTS) is a vital component of any health care delivery system. An integrated strategy for Blood Safety is required for elimination of transfusion transmitted infections and for provision of safe and adequate blood transfusion services to the people.The main component of an integrated strategy include collection of blood only from voluntary, non-remunerated blood donors, screening for all transfusion transmitted infections and reduction of unnecessary . <br>

The Blood Transfusion Service in the country is highly decentralised and lacks many vital resources like manpower, adequate infrastructure and financial base. The main issue, which plagues blood banking system in the country, is fragmented management. The standards vary from state to state, cities to cities and centre to centre in the same city. In spite of hospital based system, many large hospitals and nursing homes do not have their own blood banks and this has led to proliferation of stand-alone private blood banks. <br>

The blood component production/availability and utilisation is extremely limited. There is shortage of trained health-care professionals in the field of transfusion medicine. <br>

For quality, safety and efficacy of blood and blood products, well-equipped blood centres with adequate infrastructure and trained manpower is an essential requirement. For effective clinical use of blood, it is necessary to train clinical staff. To attain maximum safety the requirements of good manufacturing practices and implementation of quality system moving towards total quality management, have posed a challenge to the organisation and management of blood transfusion service. <br>

Thus, a need for modification and change in the blood transfusion service has necessitated formulation of a National Blood Policy and development of a National Blood Programme which will also ensure implementation of the directives of Supreme Court of India -1996.


